namespace TrueCraft.Client.Rendering
{
    /// <summary>
    ///     Enumerates the styles of fonts.
    /// </summary>
    public enum FontStyle
    {
        /// <summary>
        ///     Regular font.
        /// </summary>
        Regular = 0,

        /// <summary>
        ///     Bolded font.
        /// </summary>
        Bold = 1,

        /// <summary>
        ///     Striked-through font.
        /// </summary>
        Strikethrough = 2,

        /// <summary>
        ///     Underlined font.
        /// </summary>
        Underline = 3,

        /// <summary>
        ///     Italicized font.
        /// </summary>
        Italic = 4
    }
}